package Usuario

class Empresa {
    String nome, email, cnpj, pais, cep, descricao, senha

    Empresa(String nome, String email, String cnpj, String pais, String cep, String descricao, String senha) {
        this.nome = nome
        this.email = email
        this.cnpj = cnpj
        this.pais = pais
        this.cep = cep
        this.descricao = descricao
        this.senha = senha
    }
}
